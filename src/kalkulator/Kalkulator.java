    public void equal() {
        switch(pilih){
        case 1:
            bil2 = Double.parseDouble(bilangan);
            bil1 = bil1 + bil2;
            bilangan = Double.toString(bil1);
            layar.setText(bilangan);
            break;
        case 2:
            bil2 = Double.parseDouble(bilangan);
            bil1 = bil1 - bil2;
            bilangan = Double.toString(bil1);
            layar.setText(bilangan);
            break;
        case 3:
            bil2 = Double.parseDouble(bilangan);
            bil1 = bil1 * bil2;
            bilangan = Double.toString(bil1);
            layar.setText(bilangan);
            break;
        case 4:
            bil2 = Double.parseDouble(bilangan);
            bil1 = bil1 / bil2;
            bilangan = Double.toString(bil1);
            layar.setText(bilangan);
            break;
        case 5:
            bil2 = Double.parseDouble(bilangan);
            bil1 = bil1 % bil2;
            bilangan = Double.toString(bil1);
            layar.setText(bilangan);
            break;
        default:
            break;
        }
    pilih = 0;
    }
    
